def cipher1Inverse(j, input):
    input &= 7;
    return (j >> input) | (j << (8-input)) & 0xFF

def cipher2Inverse(j, input):
    input &= 7;
    return (j << input) | (j >> (8-input)) & 0xFF

def cipher3Inverse(input, i):
    input = (input - 42) & 0xFF
    input = cipher1Inverse(input, (i+3) %7)
